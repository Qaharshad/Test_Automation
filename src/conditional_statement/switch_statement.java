package conditional_statement;

public class switch_statement {

	public static void main(String[] args) {
		
		char ch = 'p';
		
		switch(ch) 
		{
			case  'a':
			System.out.println("its vowel");
			break;
			
			case  'e':
				System.out.println("its vowel");
				break;
			case  'i':
				System.out.println("its vowel");
				break;
			case  'o':
				System.out.println("its vowel");
				break;
			case  'u':
				System.out.println("its vowel");
				break;
		default : 
			System.out.println("Not a vowel");
		}
			
		}
	}


